#!/usr/bin/env node
/**
 * Generates src/lib/scripts-data.ts — the full JLPT N5–N3 kanji sets and
 * HSK 1–3 hanzi sets — from authoritative open datasets. Run manually:
 *
 *   node tools/gen-scripts-data.mjs
 *
 * Data sources (attribution required — see the header it writes):
 *  - Kanji readings/meanings: KANJIDIC2 © EDRDG, CC BY-SA 4.0, via
 *    github.com/davidluzgouveia/kanji-data (which adds Tanos JLPT levels).
 *  - HSK 2.0 word lists: github.com/drkameleon/complete-hsk-vocabulary.
 *  - Hanzi pinyin/definitions: github.com/skishore/makemeahanzi (Unihan-derived).
 */
import { writeFileSync } from 'node:fs';

const KANJI_URL = 'https://raw.githubusercontent.com/davidluzgouveia/kanji-data/master/kanji.json';
const MMAH_URL = 'https://raw.githubusercontent.com/skishore/makemeahanzi/master/dictionary.txt';
const HSK_URL = (lv) => `https://raw.githubusercontent.com/drkameleon/complete-hsk-vocabulary/main/wordlists/exclusive/old/${lv}.json`;

const HAN = /[一-鿿]/;

async function getJSON(url) {
	const r = await fetch(url);
	if (!r.ok) throw new Error(`${url} → ${r.status}`);
	return r.json();
}
async function getText(url) {
	const r = await fetch(url);
	if (!r.ok) throw new Error(`${url} → ${r.status}`);
	return r.text();
}

// --- Kanji ------------------------------------------------------------------

function cleanReading(r) {
	r = r.replace(/^-/, '');
	const dot = r.indexOf('.');
	if (dot >= 0) r = r.slice(0, dot);
	return r.replace(/[-.]/g, '').trim();
}

function kanjiReading(e) {
	const pick = (arr) =>
		[...new Set((arr || []).filter((r) => !r.startsWith('-')).map(cleanReading).filter(Boolean))].slice(0, 2);
	const on = pick(e.readings_on);
	const kun = pick(e.readings_kun);
	const parts = [];
	if (on.length) parts.push(on.join('・'));
	if (kun.length) parts.push(kun.join('・'));
	return parts.join(' / ') || '—';
}

function kanjiMeaning(e) {
	return (e.meanings || []).slice(0, 3).map((m) => m.toLowerCase()).join(', ') || '—';
}

async function buildKanji() {
	const data = await getJSON(KANJI_URL);
	const byLevel = { 5: [], 4: [], 3: [] };
	for (const [char, e] of Object.entries(data)) {
		const lv = e.jlpt_new;
		if (lv !== 5 && lv !== 4 && lv !== 3) continue;
		byLevel[lv].push({ char, roman: kanjiReading(e), gloss: kanjiMeaning(e), freq: e.freq ?? 99999 });
	}
	for (const lv of [5, 4, 3]) {
		byLevel[lv].sort((a, b) => a.freq - b.freq);
		byLevel[lv] = byLevel[lv].map(({ char, roman, gloss }) => ({ char, roman, gloss }));
	}
	return byLevel;
}

// --- Hanzi ------------------------------------------------------------------

function cleanDef(def) {
	if (!def) return '';
	let d = def.split(';')[0]; // first sense
	d = d
		.split(',')
		.map((s) => s.trim())
		.filter(Boolean)
		.slice(0, 3)
		.join(', ');
	if (d.length > 44) d = d.slice(0, 44).replace(/[, ]+$/, '') + '…';
	return d;
}

async function buildHanzi() {
	// Char → {pinyin, def} from makemeahanzi.
	const mmahText = await getText(MMAH_URL);
	const charInfo = new Map();
	for (const line of mmahText.split('\n')) {
		if (!line.trim()) continue;
		try {
			const o = JSON.parse(line);
			if (o.character && HAN.test(o.character)) {
				charInfo.set(o.character, { pinyin: (o.pinyin || [])[0] || '', def: cleanDef(o.definition) });
			}
		} catch {
			/* skip */
		}
	}

	const seen = new Set();
	const levels = {};
	for (const lv of [1, 2, 3]) {
		const words = await getJSON(HSK_URL(lv));
		// Single-char HSK words give an authoritative pinyin + English gloss.
		const single = new Map();
		for (const w of words) {
			const s = w.simplified || '';
			const form = (w.forms && w.forms[0]) || {};
			if ([...s].length === 1 && HAN.test(s)) {
				single.set(s, {
					pinyin: form.transcriptions?.pinyin || '',
					def: cleanDef(form.meanings?.[0] || '')
				});
			}
		}
		// Collect chars first introduced at this level, in word order.
		const chars = [];
		for (const w of words) {
			for (const ch of w.simplified || '') {
				if (!HAN.test(ch) || seen.has(ch)) continue;
				seen.add(ch);
				const info = single.get(ch) || charInfo.get(ch) || {};
				const pinyin = info.pinyin || charInfo.get(ch)?.pinyin || '';
				const def = (info.def && info.def.length <= 44 ? info.def : '') || charInfo.get(ch)?.def || '';
				chars.push({ char: ch, roman: pinyin || '—', gloss: (def || '—').slice(0, 44) });
			}
		}
		levels[lv] = chars;
	}
	return levels;
}

// --- Emit -------------------------------------------------------------------

function arr(name, items) {
	return `export const ${name}: Glyph[] = ${JSON.stringify(items)};\n`;
}

const kanji = await buildKanji();
const hanzi = await buildHanzi();

const header = `// AUTO-GENERATED by tools/gen-scripts-data.mjs — do not edit by hand.
//
// Full JLPT N5–N3 kanji and HSK 1–3 hanzi reference sets, built from open data:
//  • Kanji readings & meanings: KANJIDIC2 © EDRDG (CC BY-SA 4.0), with Tanos
//    JLPT levels, via github.com/davidluzgouveia/kanji-data
//  • HSK 2.0 word lists: github.com/drkameleon/complete-hsk-vocabulary
//  • Hanzi pinyin & definitions: github.com/skishore/makemeahanzi (Unihan-derived)
//
// eslint-disable
/* prettier-ignore */
import type { Glyph } from './scripts';

`;

const out =
	header +
	arr('KANJI_N5', kanji[5]) +
	arr('KANJI_N4', kanji[4]) +
	arr('KANJI_N3', kanji[3]) +
	arr('HANZI_HSK1', hanzi[1]) +
	arr('HANZI_HSK2', hanzi[2]) +
	arr('HANZI_HSK3', hanzi[3]);

writeFileSync('src/lib/scripts-data.ts', out);
console.log(
	`Wrote src/lib/scripts-data.ts — kanji N5/N4/N3 = ${kanji[5].length}/${kanji[4].length}/${kanji[3].length}, hanzi HSK1/2/3 = ${hanzi[1].length}/${hanzi[2].length}/${hanzi[3].length}`
);
